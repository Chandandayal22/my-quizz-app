/*-----------------------------------------timer-----------------------------------*/

let timeValue = 15;
let counter;
function startTimer(time) {
   counter = setInterval(timer, 1000);
  function timer() {
    timerStart.textContent = time;
    time--;
    if (time < 5) {
      timerStart.style.color = "red";
    }
    if (time < 0) {
      clearInterval(counter);
      timerStart.textContent = "00";
      currentQuiz++
    }
  }
}



/*------------------------------------timer---------end--------------------------------*/



const deselectAnswers = () => {
  answerElements.forEach((answer) => (answer.checked = false));
};
const getSelected = () => { 
    let answer;
  answerElements.forEach((answerElement) => {
    if (answerElement.checked) {
      answer = answerElement.id;
    }
});
  return answer;
};



/*------------------------------------pieps------------------------------*/


var currentQuiz = 0;
var score = 0;
function loadPipesQuiz(){
  timerStart.style.color = "white"
  clearInterval(counter);
  startTimer(timeValue);
  deselectAnswers();
  const currentQuizData = Pipes[currentQuiz];
  questionNo.innerHTML = currentQuizData.que;
  questionElement.innerText = currentQuizData.question;
  a_text.innerHTML = currentQuizData.a;
  d_text.innerHTML = currentQuizData.d;
  b_text.innerHTML = currentQuizData.b;
  c_text.innerHTML = currentQuizData.c;
};

function contPipeQuiz() {
  submitButton.addEventListener("click", () => {
    const answer = getSelected()
    if (answer) {
      if (answer === Pipes[currentQuiz].correct) {
        score++;
      }
      currentQuiz++;      
      if (currentQuiz < Pipes.length) loadPipesQuiz();
      else {
        quiz.innerHTML = `
      <h2>You answered ${score}/${Pipes.length} questions correctly</h2>
      <button onclick="history.go(0)">Play Again</button>
      `
    }
  }
  })
};


